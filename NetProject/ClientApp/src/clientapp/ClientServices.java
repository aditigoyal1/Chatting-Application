    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        // TODO add your handling code here:
        
        if(this.tblUserList.getSelectedRow() > -1){
            this.tblChatData.setVisible(true);
            this.txtChatData.setVisible(true);
            this.btnSend.setVisible(true);
            CommonRes.activeClient=this.tblUserList.getSelectedRow();
            this.reloadChatData(CommonRes.activeClient);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

