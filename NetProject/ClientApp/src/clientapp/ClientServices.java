    public void addClient(ClientDetails details){
        DefaultTableModel model=(DefaultTableModel)this.tblUserList.getModel();
        model.addRow(new Object[]{details.name});
        model.fireTableDataChanged();
    }
    public void addChatRow(ChatData chatdata){
        DefaultTableModel model=(DefaultTableModel)this.tblChatData.getModel();
        model.addRow(new Object[]{chatdata.from,chatdata.time,chatdata.msg});
        model.fireTableDataChanged();
    }
    public void reloadChatData(int clientId){
        DefaultTableModel model=(DefaultTableModel)this.tblChatData.getModel();
        model.setRowCount(0);
        ClientDetails details=CommonRes.loggedInClients.get(clientId);
        for(ChatData chatdata:details.chatdata){
            model.addRow(new Object[]{chatdata.from,chatdata.time,chatdata.msg});
        }
        model.fireTableDataChanged();
    }
    
