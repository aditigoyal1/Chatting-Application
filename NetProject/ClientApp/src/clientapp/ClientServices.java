    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        if(this.tblUserList.getSelectedRow() > -1 && this.txtChatData.getText().trim().length()>0){
            try{
                int index=this.tblUserList.getSelectedRow();
                ClientDetails details=CommonRes.loggedInClients.get(index);
                ObjectOutputStream out=new ObjectOutputStream(CommonRes.client.getOutputStream());
                out.writeObject(RequestCode.CHAT_SEND);
                out.writeObject(details.clientId);
                out.writeObject(this.txtChatData.getText().trim());
                ChatData chatdata=new ChatData();
                chatdata.from="SELF";
                chatdata.time=CommonRes.getCurrentTime();
                chatdata.msg=this.txtChatData.getText().trim();
                details.chatdata.add(chatdata);
                this.addChatRow(chatdata);
                this.txtChatData.setText("");
                
                
            }catch(Exception ex){
                CommonRes.trackException("Error while sending chat message : " + ex, this);
            }
        }
    }//GEN-LAST:event_btnSendActionPerformed

